// 转换为完整的卦象数据
export const HEXAGRAM_DATA: Record<string, HexagramData> = {};

RAW_DATA.forEach(([index, combination, name, guaXiang, xiangYue, poem, nature, wealth, health, travel, love]) => {
  const id = index.toString();

  HEXAGRAM_DATA[id] = {
    id,
    number: `第${index}卦`,
    name: `${name}卦`,
    nature,
    subTitle: guaXiang,
    description: combination, // 棋子组合，如"三下"
    xiang: xiangYue, // 象曰内容
    poem, // 诗曰内容
    guidance: {
      wealth,
      health,
      travel,
      love
    }
  };
});

// 导出便捷函数
export const getHexagramById = (id: string): HexagramData | undefined => {
  return HEXAGRAM_DATA[id];
};

export const getAllHexagrams = (): HexagramData[] => {
  return Object.values(HEXAGRAM_DATA);
};

// 获取随机卦象（用于占卜）
export const getRandomHexagram = (): HexagramData => {
  const keys = Object.keys(HEXAGRAM_DATA);
  const randomKey = keys[Math.floor(Math.random() * keys.length)];
  return HEXAGRAM_DATA[randomKey];
};
